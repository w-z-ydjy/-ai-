-- ============================================================
-- 题目表结构
-- 表名: questions
-- 引擎: InnoDB
-- 字符集: utf8mb4
-- ============================================================
DROP TABLE IF EXISTS `questions`;

CREATE TABLE `questions` (
  `id`          INT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '题目ID（主键自增）',
  `type`        ENUM('单选', '多选', '判断') NOT NULL COMMENT '题型：单选/多选/判断',
  `title`       VARCHAR(500) NOT NULL COMMENT '题干',
  `option_a`    VARCHAR(200) DEFAULT NULL COMMENT '选项A（判断题为"对"）',
  `option_b`    VARCHAR(200) DEFAULT NULL COMMENT '选项B（判断题为"错"）',
  `option_c`    VARCHAR(200) DEFAULT NULL COMMENT '选项C（多选题使用）',
  `option_d`    VARCHAR(200) DEFAULT NULL COMMENT '选项D（多选题使用）',
  `answer`      VARCHAR(10) NOT NULL COMMENT '正确答案：单选A/B/C/D；多选A,B,C...；判断对/错',
  `score`       TINYINT UNSIGNED NOT NULL DEFAULT 2 COMMENT '分值（默认2分）',
  `category`    VARCHAR(50) NOT NULL DEFAULT '党建知识' COMMENT '分类（固定为党建知识）',
  PRIMARY KEY (`id`),
  INDEX `idx_type` (`type`),
  INDEX `idx_category` (`category`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='党建知识题库表';

